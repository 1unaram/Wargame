'''
(c * 16) | (c >> 4) == data[i]
(c * 16) | (c / 16) == data[i]
High <-> Low
'''
data = [0x24, 0x27, 0x13, 0xc6, 0xc6, 0x13, 0x16, 0xe6, 0x47, 0xf5, 0x26, 0x96,
        0x47, 0xf5, 0x46, 0x27, 0x13, 0x26, 0x26, 0xc6, 0x56, 0xf5, 0xc3, 0xc3, 0xf5, 0xe3, 0xe3, 0, 0, 0, 0, 0]

bit = 0b11111111

for i in range(len(data)):
    high = bin(data[i] >> 4)
    high = int(high, 2)
    low = bin(data[i] << 4)
    low = int(low, 2)
    low &= bit

    new = low + high

    print(chr(new), end="")
